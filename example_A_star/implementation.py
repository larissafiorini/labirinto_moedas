def reconstruct_path(came_from, start, goal):
    current = goal
    path = []
    while current != start:
        path.append(current)
        current = came_from[current]
    path.append(start) # optional
    path.reverse() # optional
    return path

# A* Algorithm
# A* uses a best-first search and finds the least-cost path from a given initial node to one goal node
#How it works:
#1 - Generate a list of all possible next steps toward goal from current position
#2 - Store children in Priority Queue based on distance to goal, closest first
#3 - Select closest child and repeat until goal reacher or no more children

#Since all the children are stored, if one path dont work we can change to others

#Important points:
#How you MEASURE distance to goal
#How you GENERATE CHILDREN or possible paths

# PSEUDO CODE
# OPEN = priority queue containing START
# CLOSED = empty set
# while lowest rank in OPEN is not the GOAL:
#   current = remove lowest rank item from OPEN
#   add current to CLOSED
#   for neighbors of current:
#     cost = g(current) + movementcost(current, neighbor)
#     if neighbor in OPEN and cost less than g(neighbor):
#       remove neighbor from OPEN, because new path is better
#     if neighbor in CLOSED and cost less than g(neighbor): ⁽²⁾
#       remove neighbor from CLOSED
#     if neighbor not in OPEN and neighbor not in CLOSED:
#       set g(neighbor) to cost
#       add neighbor to OPEN
#       set priority queue rank to g(neighbor) + h(neighbor)
#       set neighbor's parent to current
#
# reconstruct reverse path from goal to start
# by following parent pointers

# There are two sets, OPEN and CLOSED.
# The OPEN set contains those nodes that are candidates for examining.
# Initially, the OPEN set contains only one element: the starting position.
# The CLOSED set contains those nodes that have already been examined.
# Initially, the CLOSED set is empty. Graphically, the OPEN set is the “frontier” and the CLOSED set is the “interior” of the visited areas.
# Each node also keeps a pointer to its parent node so that we can determine how it was found.
# There is a main loop that repeatedly pulls out the best node n in OPEN (the node with the lowest f value) and examines it.
# If n is the goal, then we’re done. Otherwise, node n is removed from OPEN and added to CLOSED.
# Then, its neighbors n′ are examined.
# A neighbor that is in CLOSED has already been seen, so we don’t need to look at it.
# A neighbor that is in OPEN is scheduled to be looked at, so we don’t need to look at it now.
# Otherwise, we add it to OPEN, with its parent set to n.

def heuristic(a, b):
    (x1, y1) = a
    (x2, y2) = b
    return abs(x1 - x2) + abs(y1 - y2)

def a_star_search(graph, start, goal):
    frontier = PriorityQueue()
    frontier.put(start, 0)
    came_from = {}
    cost_so_far = {}
    came_from[start] = None
    cost_so_far[start] = 0

    while not frontier.empty():
        current = frontier.get()

        if current == goal:
            break

        for next in graph.neighbors(current):
            new_cost = cost_so_far[current] + graph.cost(current, next)
            if next not in cost_so_far or new_cost < cost_so_far[next]:
                cost_so_far[next] = new_cost
                priority = new_cost + heuristic(goal, next)
                frontier.put(next, priority)
                came_from[next] = current

    return came_from, cost_so_far
